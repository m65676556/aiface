    for (int r = 5; r <= 6; r++) {
      for (int c = 0; c < size; c++) {
        if (base[r][c] == 2 || base[r][c] == 5) {
          base[r][c] = (r == 6) ? 2 : 1; // bottom line of eyes stays, top becomes skin
        }
      }
    }
    return base;
  }

