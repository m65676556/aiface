import 'package:flutter/material.dart';

enum Expression {
  neutral,
  happy,
  thinking,
  surprised,
  sad,
  confused,
  excited,
  sleeping,
  talking,
  listening,
}

Expression expressionFromString(String name) {
  return Expression.values.firstWhere(
    (e) => e.name == name,
    orElse: () => Expression.neutral,
  );
}

// Palette: 0=transparent, 1=skin, 2=eye, 3=mouth, 4=blush, 5=highlight, 6=shadow
class PixelPalette {
  static const List<Color> colors = [
    Colors.transparent,           // 0
    Color(0xFFFFCBA4),            // 1 skin (warm peach)
    Color(0xFF2D3436),            // 2 eye (dark)
    Color(0xFFE84393),            // 3 mouth (pink)
    Color(0xFFFFB3C6),            // 4 blush (soft pink)
    Color(0xFFFFFFFF),            // 5 highlight (white)
    Color(0xFFE8A87C),            // 6 shadow (darker peach)
  ];
}

